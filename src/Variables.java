public class Variables {
    public static void main(String[] args) {
        // Java compiler infers the variable's type when you use the "var" keyword
        var email = "user@gmail.com";
        var password = "1234567890";

        // types:
        // String
        // int
        // double
        // boolean
        // float
        // char
        String name;
        name = "John";
        int age = 33;
        double height = 1.77;
        boolean isGay = false;
        char bloodType = 'A';
        float footSize = 0.2f;
    }
}
